
def dfs(num, cnt, horse):
    global ans
    # 10번 주사위 다 돌리면 최댓값 비교 후 리턴
    if num == 10:
        ans = max(ans, cnt)
        return
    
    # 백트래킹
    for i in range(4):  # 4개의 말 비교
        x = horse[i]
        horse[i] = board[x][dice[num]]
        if horse.count(horse[i])==1 or horse[i]==21:
            dfs(num+1, cnt+number[board[x][dice[num]]], horse)
        horse[i] = x


